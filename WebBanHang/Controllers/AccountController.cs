            return View();
        }

        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel model)
        {
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser
                {
                    UserName = model.Email,
                    Email = model.Email,
                    FirstName = model.FirstName,
                    LastName = model.LastName,
                    Phone = model.Phone,
                    Birthday = model.Birthday,
                    Gender = model.Gender,
                    Address = model.Address,
                    CreatedDate = DateTime.Now
                };

                var result = await UserManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);
                    UserManager.AddToRole(user.Id, "Customer");
                    return RedirectToAction("Index", "Home");
                }
                AddErrors(result);
            }

            return View(model);
        }

        [Authorize]
        public ActionResult Profile()
        {
            var userId = User.Identity.GetUserId();
            var user = UserManager.FindById(userId);
            if (user == null) return HttpNotFound();

            var model = new ProfileViewModel
            {
                UserName = user.UserName,
                FirstName = user.FirstName,
                LastName = user.LastName,
                Email = user.Email,
                Phone = user.Phone,
                Birthday = user.Birthday,
                Gender = user.Gender,
                Address = user.Address,
                Avatar = user.Avatar,
                CreatedDate = user.CreatedDate
            };

            return View(model);
        }

        [HttpPost]
        [ValidateAntiForgeryToken]
        [Authorize]
        public ActionResult UpdateProfile(ProfileViewModel model)
        {
            if (!ModelState.IsValid)
                return View("Profile", model);

            var userId = User.Identity.GetUserId();
            var user = UserManager.FindById(userId);
            if (user == null) return HttpNotFound();

            user.FirstName = model.FirstName;
            user.LastName = model.LastName;
            user.Phone = model.Phone;
            user.Birthday = model.Birthday;
            user.Gender = model.Gender;
            user.Address = model.Address;

            UserManager.Update(user);

            TempData["Success"] = "Cập nhật thành công";
            return RedirectToAction("Profile");
        }


        [HttpPost]
        [Authorize]
        public ActionResult UploadAvatar(HttpPostedFileBase file)
        {
            if (file != null && file.ContentLength > 0)
            {
                var ext = Path.GetExtension(file.FileName);
                var fileName = $"{Guid.NewGuid()}{ext}";
                var path = Path.Combine(Server.MapPath("~/Content/uploads/avatar"), fileName);

                Directory.CreateDirectory(Server.MapPath("~/Content/uploads/avatar"));
                file.SaveAs(path);

                var userId = User.Identity.GetUserId();
                var user = UserManager.FindById(userId);
                user.Avatar = "/Content/uploads/avatar/" + fileName;
                UserManager.Update(user);

                return Json(new { success = true, avatar = user.Avatar });
            }

            return Json(new { success = false });
        }

        public ActionResult OrderHistory()
        {
            var userId = User.Identity.GetUserId(); // Lấy userId của người dùng
            var orders = db.Orders
                .Where(o => o.CustomerId == userId)
                .Include("OrderDetails.Product") // <-- thêm dòng này
                .OrderByDescending(o => o.CreatedDate)
                .ToList();

            var result = orders.Select(o => new HistoryOrderViewModels
            {
                OrderId = o.Id,
                Code = o.Code,
                CreatedDate = o.CreatedDate ?? DateTime.Now,
                TotalAmount = o.TotalAmount,
                Status = o.Status,
                PaymentMethod = o.TypePayment == 1 ? "COD" : "Chuyển Khoản",
                ShippingAddress = o.Address,
                OrderDetails = o.OrderDetails.Select(d => new OrderDetailViewModel
                {
                    ProductId = d.Product.Id,
                    ProductAlias = d.Product.Alias,
                    ProductName = d.Product.Title, // Lấy tên sản phẩm từ Product model
                    ProductImage = d.Product.Image, // Lấy hình ảnh sản phẩm từ Product model
                    Price = d.Price,
                    Quanlity = d.Quanlity
                }).ToList()
            }).ToList();

            return View(result);
        }



